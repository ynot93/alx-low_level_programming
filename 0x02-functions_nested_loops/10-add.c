/**
 * add - Adds two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Description: This function takes two integers and adds them together.
 *              It returns the sum of the two integers.
 *
 * Return: The sum of the two integers
 */
int add(int a, int b)
{
	return (a + b);
}
