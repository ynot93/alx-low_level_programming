/**
 * mul - Multiplies two integers
 * @a: First integer
 * @b: Second integer
 *
 * Description: This function takes two integers as arguments
 * and multiplies them together to give a result
 *
 * Return: The result of multiplication
 */

int mul(int a, int b)
{
	int result;

	result = a * b;
	return (result);
}
